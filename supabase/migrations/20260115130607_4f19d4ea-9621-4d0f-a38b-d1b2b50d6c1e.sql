-- Create material suppliers table
CREATE TABLE public.quotation_material_suppliers (
  id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  bp_code VARCHAR(20) NOT NULL UNIQUE,
  bp_name VARCHAR(255) NOT NULL,
  is_active BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.quotation_material_suppliers ENABLE ROW LEVEL SECURITY;

-- Create policies
CREATE POLICY "Allow authenticated users to read material suppliers"
ON public.quotation_material_suppliers FOR SELECT
TO authenticated
USING (true);

CREATE POLICY "Allow authenticated users to insert material suppliers"
ON public.quotation_material_suppliers FOR INSERT
TO authenticated
WITH CHECK (true);

CREATE POLICY "Allow authenticated users to update material suppliers"
ON public.quotation_material_suppliers FOR UPDATE
TO authenticated
USING (true);

CREATE POLICY "Allow authenticated users to delete material suppliers"
ON public.quotation_material_suppliers FOR DELETE
TO authenticated
USING (true);

-- Create trigger for updated_at
CREATE TRIGGER update_quotation_material_suppliers_updated_at
BEFORE UPDATE ON public.quotation_material_suppliers
FOR EACH ROW
EXECUTE FUNCTION public.handle_updated_at();

